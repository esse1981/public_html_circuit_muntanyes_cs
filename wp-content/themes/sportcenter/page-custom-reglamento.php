            <h3><strong><i class="fa fa-pencil-square-o" aria-hidden="true"></i> INSCRPCIONES</strong></h3>
            Los ciclistas que deseen participar en el <strong>“X CIRCUIT BTT MUNTANYES DE CASTELLO”</strong> deberán inscribirse en cada una de las pruebas a las que quieran asistir. Para ello deberán cumplimentar todos los datos personales de la ficha de inscripción y formalizar el pago.
            Con los datos de todos los participantes se creará una base de datos administrada y custodiada por el Comité Organizador.
            Se establece como norma general del <strong>“X CIRCUIT BTT MUNTANYES DE CASTELLÓ”</strong> la inscripción anticipada de los ciclistas interesados en participar en cada una de las pruebas, fijando el límite para realizar las inscripciones en las 23:59 horas del jueves anterior al día de celebración de la marcha.
            La inscripción se realizará a través de la siguiente dirección Web:
            http://hj-crono.es/circuitos/

            <br />
            EN NINGÚN CASO SE REALIZARÁN INSCRIPCIONES EL MISMO DÍA DE LA PRUEBA.
            <br />
            El importe de la inscripción individual en cada una de las pruebas que componen el “X CIRCUIT BTT MUNTANYES DE CASTELLÓ” será el siguiente:
            <br />
            <strong>Federado 18 € / No Federado 22 €</strong>
            <br />
            Las inscripciones que se realicen entre las 00:00 h del lunes y las 23:59 h del jueves anterior a la celebración de la prueba:
            <br />
            <strong>Federado 23 € / No Federado 27 €</strong>
            <br />
            Todas las inscripciones serán sin obsequio, quedando a criterio de la Organización ofrecer un detalle genérico para todos los participantes en todas las marchas.
            <br />
            Las inscripciones de grupos de diez o más participantes serán obsequiadas con un jamón.
            <br />
            Este año vamos a poner a la venta una cantidad limitada de 50 Bonos-Inscripción por un precio de 110,00€ para Federados y 130,00€ para No Federados. Una vez se agoten los Bonos de esta oferta el precio será de 125,00€ para Federados y de 155,00€ para No Federados, hasta las 23:59 h. del día 15 de marzo.
            <br />
            A todo participante que se inscriba mediante esta modalidad (Bono), se le asignará un dorsal en propiedad, que será el mismo para todas las pruebas. Esta modalidad de inscripción podrá realizarse a través de la plataforma de pago hasta el 15 de marzo a las 23:59 h.
            <br />
            La diferencia económica entre Federados y No Federados, se debe al coste del seguro de accidentes y R.C. que la Organización contrata para los participantes no federados y cuyas cláusulas aceptan los mismos en el momento de la inscripción.
            <br />
            <u>EN NINGÚN CASO SE PREVÉ LA DEVOLUCIÓN DE LAS CANTIDADES ABONADAS.</u>
            <br />
            Todos los participantes deberán firmar el correspondiente control de firma en el momento de la entrega del dorsal, <strong>identificándose con su DNI y/o Tarjeta federativa</strong>, ante el organizador, que será responsable de verificar su identidad.
            <br />
            Los participantes Federados, deberán mostrar <strong>obligatoriamente </strong>su Licencia Federativa, en caso de no presentar dicha Licencia, el organizador amonestará al participante y le exigirá que firme una declaración jurada donde el corredor asumirá toda la responsabilidad sobre los daños propios o ajenos que pudiera recibir o causar durante la prueba.
            Dicha firma supone la aceptación del presente Reglamento, así como del particular de cada una de las pruebas si los hubiere.
            <br />
            La organización del Circuit dispone de un seguro de Accidentes y Responsabilidad Civil que cumple las condiciones de la normativa vigente, el cual los participantes aceptan y contra el cual se comprometen a no interponer reclamación o denuncia alguna en el supuesto de que los gastos de accidente fuesen superiores a la cifra contratada.

