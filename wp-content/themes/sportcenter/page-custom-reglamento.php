            <h3><strong><i class="fa fa-align-left" aria-hidden="true"></i> INTRODUCCIÓN</strong></h3>
            El <strong>“CIRCUIT MARXES BTT CASTELLÓ”</strong> nació en el año 2011, con la idea de ofrecer un calendario de marchas ciclo deportivas de BTT en distintos municipios de nuestra provincia con unos criterios y servicios comunes para mejorar la calidad de nuestras actividades deportivas.

            Por ello, el “CIRCUIT” quiere mejorar con objeto de impulsar el turismo de interior, modifica su nombre relacionándolo con nuestras montañas y pasa a denominarse “CIRCUIT BTT MUNTANYES DE CASTELLÓ” teniendo como finalidad dar a conocer el interior de la provincia de Castellón y promover la práctica popular de un ciclismo de montaña sostenible y responsable, intentando servir de impulso a la economía local de sus municipios.

            Varios Clubes y Asociaciones de BTT de la provincia de Castellón son los que organizan el “X CIRCUIT BTT MUNTANYES DE CASTELLÓ” en el que podrán participar todos los ciclistas de montaña que lo deseen, compuesto por una serie de marchas ciclo deportivas que empezarán el 29 de marzo y finalizarán el 27 de septiembre. En la presente edición pretendemos incluir varias novedades respecto al desarrollo de las pruebas:
            <ul>
                <li>Se mantiene la Categoría por Equipos, donde puntuarán los tres primeros clasificados en cada prueba. Los integrantes de cada equipo deberán permanecer en el mismo desde el inicio hasta la finalización de la presente edición del Circuit.</li>
                <li>No se les permitirá cambiar de equipo y deberán realizar las nueve Marchas. Los equipos tendrán un reconocimiento al finalizar cada prueba y se entregará trofeo al vencedor a la finalización del Circuit en el Acto de Clausura.</li>
                <li>Se consolida la categoría exclusiva para las e-bikes y se actualiza el reglamento.</li>
                <li>Se ponen a la venta 50 Bono-Inscripciones a un precio inferior al habitual, como una oferta puntual dirigida a todos los bikers.</li>
                <li>Se actualiza las puntuaciones del reglamento.</li>
            </ul>
            Los corredores obtendrán una clasificación individual de cada prueba y una clasificación general del Circuit, dividida por categorías, en la que se computarán los ocho mejores resultados de las nueve pruebas.

            El Comité Organizador (órgano compuesto por representantes de los clubes y asociaciones organizadoras) velará por el cumplimiento de este Reglamento, siendo responsabilidad de cada entidad organizadora el correcto desarrollo de su prueba.

            Este Comité Organizador se reserva el derecho a modificar el presente Reglamento si así lo considerara oportuno, debiendo informar a los participantes de los cambios realizados.

