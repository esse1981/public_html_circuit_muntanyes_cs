            <hr />
            <h3><strong><i class="fa fa-trophy" aria-hidden="true"></i> PUNTUACIONES</strong></h3>
            <img class="img-responsive" src="/wp-content/uploads/2014/09/tabla_puntuaciones.jpg">
            <br />
            Será requisito indispensable a efectos clasificatorios y de puntuaciones pasar sobre la alfombra de cronometraje, tanto a la salida como a la llegada a meta. A no ser que haya abandona la prueba por cualquier motivo, en este caso deberá avisar a la organización y al crono de la prueba de dicho abandono.
          <br />
            La puntuación se asignará según el orden de llegada a meta, cada participante obtendrá un número de puntos dependiendo del puesto obtenido en la clasificación de su categoría.
            Una vez finalizada cada una de las pruebas, se procederá a hacer pública, en la Web del Circuit, tanto la Clasificación Individual de la prueba correspondiente como la actualización de la Clasificación General por Categorías del Circuit.
            <br />
            <br />
            Será necesario participar en ocho de las nueve pruebas para tener opción a la Clasificación Final del Circuit, y se computarán los ocho mejores resultados obtenidos en las pruebas realizadas por el participante. En caso de suspensión de alguna prueba, será el Comité Organizador el que decida el número de resultados computables.
            En caso de empate se tendrá en cuenta, a igual número de pruebas, el mayor número de primeros puestos, segundos, etc.
            <br />
            En caso de que en una categoría existan más de 150 clasificados, se puntuará con 1 punto a todos los que clasifiquen por encima de dicho puesto.
            <br />
            En la última prueba del <strong>“CIRCUIT”, XXVIII MARXA BTT FIRA D'ONDA – ONDA</strong>, todos los participantes que hayan realizado las anteriores 8 pruebas recibirán un 25% de bonificación en la clasificación final de la marcha.
            <br />
            <br />
            Los participantes que abandonen una prueba por avería, caída o cualquier otra causa justificada, puntuarán con 25 puntos menos que el último clasificado en su categoría en dicha prueba.
            En esta edición, mantenemos la entrega de maillots a los vencedores de cada categoría en las diferentes marchas. El líder de cada categoría portará el correspondiente maillot.

            <a name="detalles" id="detalles"></a>
            <hr />
            <h3><strong><i class="fa fa-search" aria-hidden="true"></i> DETALLES</strong></h3>
            Todas las marchas obsequiarán como mínimo, a los tres primeros clasificados absolutos y a los tres primeros puestos de cada categoría.
            Cada Organizador puede establecer otros obsequios.
            Al finalizar se celebrará un Acto de Clausura, donde se procederá a la entrega de obsequios y reconocimientos a los participantes.
            <br />
            La Organización quiere reconocer la participación, por tanto, obsequiará a los participantes de acuerdo a los siguientes criterios:
            <ul>
                <li>Por asistencia, a todos los participantes que se hayan inscrito en las nueve pruebas, recibirán el obsequio que la organización decida.</li>
                <li>Por finalización, a todos los participantes que hayan finalizado las nueve pruebas, obtendrán un Diploma Finisher.</li>
            </ul>
            Ambos reconocimientos son compatibles.

            <a name="descalificaciones" id="descalificaciones"></a>
            <hr />
            <h3><strong><i class="fa fa-thumbs-down" aria-hidden="true"></i> DESCALIFICACIONES</strong></h3>
            La descalificación de un ciclista en cualquiera de las pruebas, supone la pérdida de todos los puntos y obsequios obtenidos por el infractor en dicha prueba. Computando como no presentado.
            Serán motivos de descalificación del “X CBTTMCS”:
            <ul>

                <li>No tomar la salida desde la línea de Salida.</li>
                <li>No hacer uso del casco protector durante el transcurso de la prueba, siendo su uso obligatorio en todas las marchas.</li>

            </ul>


