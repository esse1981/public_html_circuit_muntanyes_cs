            <hr />
            <h3><strong><i class="fa fa-battery-full" aria-hidden="true"></i> E-BIKES</strong></h3>
            El Circuit quiere adaptarse a la evolución del mercado de la bicicleta creando una categoría dedicada exclusivamente a las e-Bikes. La organización realizará una clasificación única para dicha categoría, común para hombres y mujeres, sin distinción de edad, en la que se entregará trofeo a los tres primeros clasificados, clasificación Scratch, utilizando el mismo sistema de puntuación que para el resto de categorías.
            <br />
            En el caso de que existieran varios recorridos en una prueba, los participantes en esta modalidad siempre lo harán en el recorrido largo. Los participantes en esta categoría deberán de indicarlo al formalizar la inscripción. Tomarán la salida en primer lugar, aunque cada organizador podrá decidir sobre ello según las características de su prueba.
            <br />
            Se limita la participación a las bicicletas de pedaleo asistido tipo Pedelec, que sólo se activará cuando se pedalea y debe dejar de actuar al dejar de pedalear (por lo tanto, no pueden tener acelerador) o cuando la velocidad supere los 25 Km/h, el incumplimiento de alguna de estas condiciones supondrá la descalificación del participante.
            Antes de tomar la salida, todas las e-Bikes pasarán una verificación previa y se precintará la batería y la conexión de recarga.
            <br />
            El recorrido se deberá completar con una sola carga de batería, siendo causa de descalificación directa el incumplimiento de esta condición.

